			<h2>Our Story</h2>

			<p>Stu.Accom is a website service created by six students in the School of Computing and Engineering at The University of Huddersfield.
			Our aim is to create a website that allows students to easily locate all the accommodation options near their chosen university.
			As this is a project being developed in a short timeframe, we have decided to scale down the operation and focus on student flats
			in the Huddersfield area.</p>

			<p>We hope to make our website the one stop shop for accommodations. Unlike our competitors, we will have up to date information, include
			all the facilities available, a map which locates local shops, takeaways, and social activities. Our user account system will also
			allow for students to add accommodations to their favourites, view, and sign contracts, apply for accommodations, and leave reviews.</p>

			<p>The landlord portal will allow landlords to add, edit and remove accommodations. They will also have the ability to add and remove promotions and view, accept and deny applications. Giving landlords the ability to do all this makes our website appealing to them, as it removes the need
			for them to send emails to the website administrators to make changes, giving them more independence.</p>
